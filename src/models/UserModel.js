export class User {
    constructor(
        id,
        username,
        firstName,
        lastName,
        email,
        mobile,
        phone,
        entity
    ) {
        this.id = id;
        this.username = username;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.mobile = mobile;
        this.phone = phone;
        this.entity = entity;
    }
}
